public class IRCPeer 
{
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// private instance members
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  private String _nick;
  private String _user; 
  private String _name;
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// constructors 
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  public IRCPeer(String nick, String user, String name)
  {
    this._nick = nick;
    this._user = user;
    this._name = name;
  }
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// accessor/mutator methods for private instance members
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String nick()
  {
    return this._nick;
  }
  
  public String user()
  {
    return this._user;
  }
  
  public String name()
  {
    return this._name;
  }
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// overridden methods declared in super class
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  @Override
  public String toString()
  {
    return 
      "IRCPeer(" + this.hashCode() + ")=" +
      Util.q(
        Util.join(
          this.nick(),
          this.user(),
          this.name()
        )
      );
  }
}
