public class IRCChan 
{
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// private instance members
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  private String _name;
  private String _pass;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// constructors 
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  public IRCChan(String name, String pass)
  {
    this._name = name;
    this._pass = pass;
  }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// accessor/mutator methods for private instance members
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public String name()
  {
    return this._name;
  }
  
  public String pass()
  {
    return this._pass;
  }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// overridden methods declared in super class
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  @Override
  public String toString()
  {
    return 
      "IRCChan(" + this.hashCode() + ")=" +
      Util.q(
        Util.join(
          this.name(),
          this.pass()
        )
      );    
  }
}
