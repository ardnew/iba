public class IRCUser extends IRCPeer
{
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// constructors 
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  public IRCUser(String nick, String user, String name)
  {
    super(nick, user, name);
  }
  
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// overridden methods declared in super class
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  @Override
  public String toString()
  {
    return 
      "IRCUser(" + this.hashCode() + ")=" +
      Util.q(
        Util.pjoin(
          Util.q("nickname=" + this.nick()),
          Util.q("username=" + this.user()),
          Util.q("realname=" + this.name())
        )
      );
  }  
}
